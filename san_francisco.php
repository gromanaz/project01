    <h2 class="w3-center w3-padding-48"><span class="w3-tag w3-wide">Economy</span></h2>

    <p>
      San Francisco has a diversified service economy, with employment spread across a wide range of professional services, including financial services, tourism, and (increasingly) high technology. In 2016, approximately 27% of workers were employed in professional business services; 14% in leisure and hospitality; 13% in government services; 12% in education and health care; 11% in trade, transportation, and utilities; and 8% in financial activities. In 2019, GDP in the five-county San Francisco metropolitan area grew 3.8% in real terms to $592 billion.
    </p>

    <p>
      Additionally, in 2019 the 14-county San Jose - San Francisco - Oakland combined statistical area had a GDP of $1.086 trillion, ranking 3rd among CSAs, and ahead of all but 16 countries. As of 2019, San Francisco County was the 7th highest-income county in the United States (among 3,142), with a per capita personal income of $139,405. Marin County, directly to the north over the Golden Gate Bridge, and San Mateo County, directly to the south on the Peninsula, were the 6th and 9th highest-income counties respectively.
    </p>


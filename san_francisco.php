    <div id="Climate" class="w3-container menu w3-padding-48 w3-card">
      <p>
        San Francisco has a warm-summer Mediterranean climate characteristic of California's coast, with moist mild winters and dry summers. San Francisco's weather is strongly influenced by the cool currents of the Pacific Ocean on the west side of the city, and the water of San Francisco Bay to the north and east. This moderates temperature swings and produces a remarkably mild year-round climate with little seasonal temperature variation.
      </p>

      <p>
        Among major U.S. cities, San Francisco has the coolest daily mean, maximum, and minimum temperatures for June, July, and August. During the summer, rising hot air in California's interior valleys creates a low pressure area that draws winds from the North Pacific High through the Golden Gate, which creates the city's characteristic cool winds and fog. The fog is less pronounced in eastern neighborhoods and during the late summer and early fall. As a result, the year's warmest month, on average, is September, and on average, October is warmer than July, especially in daytime.
      </p>
    </div>

    <div id="Trans" class="w3-container menu w3-padding-48 w3-card">
      <p>
        Transit is the most used form of transportation every day in San Francisco. Every weekday, more than 560,000 people travel on Muni’s 69 bus routes and more than 140,000 customers ride the Muni Metro light rail system. 32% of San Francisco residents use public transportation for their daily commute to work, ranking it first on the West Coast and third overall in the United States. The San Francisco Municipal Railway, primarily known as Muni, is the primary public transit system of San Francisco.
      </p>

      <p>
        Muni is the seventh-largest transit system in the United States, with 210,848,310 rides in 2006. The system operates a combined light rail and subway system, the Muni Metro, as well as large bus and trolley coach networks. Additionally, it runs a historic streetcar line, which runs on Market Street from Castro Street to Fisherman's Wharf. It also operates the famous cable cars, which have been designated as a National Historic Landmark and are a major tourist attraction.
      </p>

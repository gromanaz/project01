    <div id="History" class="w3-container menu w3-padding-48 w3-card">
      <img src="assets/san_francisco_1846.jpg" alt="San Francisco - History">
      <p>
        The earliest archaeological evidence of human habitation of the territory of the city of San Francisco dates to 3000 BC. The Yelamu group of the Ohlone people resided in a few small villages when an overland Spanish exploration party, led by Don Gaspar de Portolá, arrived on November 2, 1769, the first documented European visit to San Francisco Bay.
      </p>
      <p>
        The first maritime presence occurred on August 5, 1775, when San Carlos - commanded by Juan Manuel de Ayala - became the first ship to anchor in the bay. The following year, on March 28, 1776, the Spanish established the Presidio of San Francisco, followed by a mission, Mission San Francisco de Así­s (Mission Dolores), established by the Spanish explorer Juan Bautista de Anza.
      </p>
    </div>

    <div id="Attraction" class="w3-container menu w3-padding-48 w3-card">

      <h4>Golden Gate Bridge</h4>
      <img src="assets/san_francisco_golden_gate_bridge.jpg" alt="San Francisco - Attraction">

      <p>
        The Golden Gate Bridge's vaulting, orange arches amidst the rocky seascape of the San Francisco Bay have made it one of the West Coast's most enduring symbols and the city's most popular tourist attraction. The bridge's name, "Golden Gate," actually refers to the body of water it spans (the Golden Gate Strait that connects the Pacific Ocean with the San Francisco Bay), and was built to make travel between San Francisco and Marin County an easier feat.
      </p>

      <p>
        There are plenty of great spots to capture a snap of the majestic bridge. But if you want a truly postcard-worthy shot, head to the Golden Gate Bridge Vista Point, situated high on a hill overlooking San Francisco. If you have extra time, make sure to explore the Golden Gate National Recreation Area. The actual span of the Golden Gate National Recreation Area encompasses multiple places in San Mateo (south of San Francisco), San Francisco and Marin Counties (Alcatraz and Muir Woods included), but notable parts of this recreation area can be found just a stroll away from the Golden Gate. From the bridge, travelers will find some scenic, bayside trails, some of which lead to secluded beaches, including Kirby Cove and Black Sands Beach. If you really want a trek, journey to the Point Bonita Lighthouse for sweeping views of the bay, found at the very tip of the Golden Gate Strait.
      </p>
    </div>

    <h2 class="w3-center w3-padding-48"><span class="w3-tag w3-wide">Cuisine</span></h2>

    <h3>Mission-Style Burritos</h3>

    <img src="assets/san_francisco_burritos.jpg" alt="San Francisco - Cuisine">

    <p>
      The Mission-style burrito — or San Francisco burrito — was created in the 1960s in The City’s Mission District. There are two theories as to when and where the first one was served: September 26, 1961 at El Faro, or at September 29, 1969 at Taqueria La Cumbre. Regardless of who was its originator, the Mission-style burrito has become an indispensable native food for San Franciscans.
    </p>

    <p>
      What sets it apart from the original meat, rice, and beans variety, is that this monster gets super-sized with the addition of extra rice, vegetables, sour cream, and cheese (in a super burrito), and wrapped in a steamed tortilla to keep all the goodness in. Besides El Faro and La Cumbre, another popular burrito institution one should visit to get the traditional Mission style burrito experience is La Taqueria.
    </p>

    <h2 class="w3-center w3-padding-48"><span class="w3-tag w3-wide">Festival</span></h2>

    <h3>How Weird Street Faire</h3>

    <img src="assets/san_francisco_festival.jpg" alt="San Francisco - Festivities">

    <p>
      The How Weird Street Faire is an outdoor street fair held each year in San Francisco, occupying several blocks of Howard Street and the surrounding area, in the SoMa neighborhood. The event has been held yearly since 2000, and is currently centered at the intersection of Howard and Second Streets.
    </p>

    <p>
      The How Weird Street Faire claims to be the longest-running electronic music street festival in North America, showcasing diverse forms of dance music including live electronica, downtempo, breaks, electro, trance, house, techno, dubstep, drum & bass, dub, and world beat. As of 2017, it had several thousand visitors, many of which come dressed in costumes.
    </p>

